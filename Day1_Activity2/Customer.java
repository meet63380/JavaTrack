class Customer {
	private String name, address, mobile;
	public String getName() {
		return this.name;
	}
	public void setName(String s) {
		this.name=s;
	}
	public String getAddress() {
		return this.address;
	}
	public void setAddress(String s) {
		this.address = s;
	}
	public String getMobile() {
		return this.mobile;
	}
	public void setMobile(String s) {
		this.mobile = s;
	}
	
}
